    <p>Entendendo a tecnologia não só como ferramenta, mas também como processo que transforma a realidade e que modifica a cultura, a escola está atenta às inovações tecnológicas e à maneira pelas quais elas intervêm na forma de pensar e de produzir cultura. Assim, através do uso das tecnologias educacionais, valoriza as novas formas de comunicação e de linguagem como fatores relevantes do processo ensino-aprendizagem, aproximando o universo da comunidade escolar. </p>

    <p>A partir desses pressupostos, o Colégio investe tanto na atualização dos artefatos e equipamentos, como na criação de softwares, jogos educativos e aplicativos, subsidiando a prática docente e garantindo a promoção da aprendizagem significativa.</p>

    <a name="ambientes-de-aprendizagem"></a>
    <h3 class="pb-2 mb-0">Ambientes de aprendizagem</h3>

    <p>O Colégio Rosa de Saron compreende que os espaços de aprendizagem são aqueles que possibilitam, no processo de ensino-aprendizagem, uma ação educativa de melhor qualidade, uma vez que acredita que a construção do conhecimento e sua socialização acontecem além das salas de aula/referência. Dessa forma, a Escola investe em espaços ricos de estímulos para a melhoria do ensino e, consequentemente, da aprendizagem. O C.R.S conta com os seguintes ambientes de aprendizagem: </p>

    <p>1. Auditório com capacidade para 60 pessoas: espaço onde ocorrem as sessões de oratória, as sessões cívicas, as comemorações de datas festivas e outras atividades que necessitam de espaço para agrupamentos maiores. </p>

    <p>2. Ambiente Virtual de Aprendizagem - AVA: é uma plataforma LMS (Sistema de Gerenciamento de Aprendizagem) que dá suporte ao ensino e à aprendizagem atendendo à toda a comunidade escolar. </p>
    <p>3. Complexo esportivo: esse ambiente é utilizado para as aulas de Educação Física, para práticas desportivas e atividades destinadas à comunidade.</p>

    <p>4. Laboratórios de informática e tecnologias educacionais: neles são desenvolvidos projetos educacionais curriculares e extracurriculares, bem como são disponibilizados horários para os estudantes, os professores e os funcionários. </p>

    <p>5. Laboratórios de robótica: desenvolvem projetos interdisciplinares, utilizando kits da Lego, revistas e manuais. Os laboratórios, específicos para as diferentes faixas etárias, são equipados com computadores, rede wireless e software apropriado. Neles também são desenvolvidos conhecimentos de mecânica, eletricidade e programação. </p>

    <p>6. Laboratório de Ciências/Física/Química: desenvolve aulas práticas de pesquisa envolvendo todos os estudantes da escola, sendo utilizado como extensão das aulas de Ciências, Química, Física e Biologia em todas as etapas de ensino. Conta com professores com horas exclusivas para o atendimento aos professores regentes e aos estudantes. </p>

    <p>7. Biblioteca: visa, prioritariamente, ao atendimento de estudantes e de professores em suas necessidades básicas de pesquisa e enriquecimento ou complementação de trabalhos. Possui um acervo diversificado e periodicamente atualizado.</p>

    <p>8. Brinquedoteca: é um espaço apropriado para atender à diversidade dos interesses das crianças. Conta com um pequeno parque coberto e cantinhos especiais para brincadeiras, leituras e jogos pedagógicos. </p>

    <p>9. Cantina: ambientes que servem aos estudantes e funcionários, bem como onde se prestam a atividades pedagógicas pertinentes. </p>

    <p>10. Parque: espaço coberto, com brinquedos para interações e situações auto organizadas das crianças pequenas.</p>

